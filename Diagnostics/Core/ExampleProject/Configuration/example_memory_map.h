#include "internalFlash_STM32F205VG.h"

/* Example Memory Map
REGION          START        SIZE           KB   SECTOR(S)
Bootloader      0x08000000   0x00008000     32       0-1
Mftg Params     0x08008000   0x00004000     16       2
User Params     0x0800C000   0x00004000     16       3
Logger Store    0x08010000   0x00010000     64       4
Recovery Img    0x08020000   0x00040000    256       5-6
Application     0x08060000   0x000A0000    640       7-11
*/

