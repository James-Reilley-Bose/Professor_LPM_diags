#define INTERNAL_FLASH_DEBUG_LOGGING_START       INTERNAL_FLASH_SECTOR4_START_ADDRESS
#define INTERNAL_FLASH_DEBUG_LOGGING_END         INTERNAL_FLASH_SECTOR4_END_ADDRESS

#define INTERNAL_FLASH_RECOVERY_IMG_START        INTERNAL_FLASH_SECTOR5_START_ADDRESS
#define INTERNAL_FLASH_RECOVERY_IMG_END          INTERNAL_FLASH_SECTOR6_END_ADDRESS

#define INTERNAL_FLASH_APPLICATION_START         INTERNAL_FLASH_SECTOR7_START_ADDRESS
#define INTERNAL_FLASH_APPLICATION_END           INTERNAL_FLASH_SECTOR11_END_ADDRESS

